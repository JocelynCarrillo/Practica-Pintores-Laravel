<center><H3>Datos generales</H3> </center>
<p > Chilling Adventures of Sabrina (titulada El mundo oculto de 
Sabrina en Hispanoamérica) y es una serie de televisión estadounidense de 
misterio sobrenatural desarrollada por Roberto Aguirre-Sacasa para Netflix, basada en la serie de 
cómics del mismo nombre. <br> La serie es producida por Warner Bros. Television, en asociación con Berlanti 
Productions y Archie Comics. Aguirre-Sacasa y Greg Berlanti sirven como productores ejecutivos, junto 
a Sarah Schechter, Jon Goldwater y Lee Toland Krieger. <br>

La serie se centra en el personaje de Archie Comics Sabrina Spellman. Se desarrolló 
originalmente durante septiembre de 2017 para The CW, la serie estaba destinada a ser una serie 
complementaria de la serie de The CW Riverdale; sin embargo, en diciembre de 2017, el proyecto se 
trasladó a Netflix con un pedido directo de serie, confirmando dos temporadas. <br> El rodaje tiene lugar 
en Vancouver, Columbia Británica, donde ambas partes de la primera temporada se rodaron consecutivamente. <br>

Los primeros diez episodios se lanzaron el 26 de octubre de 2018. La serie recibió críticas positivas,
y los críticos elogiaron el desempeño de Shipka, así como la premisa, las imágenes y la dirección. <br>
El 14 de diciembre de 2018 se lanzó un episodio especial de Navidad, y la segunda parte de la primera
temporada se lanzó el 5 de abril de 2019. En diciembre de 2018, Netflix renovó la serie para una segunda 
temporada de 16 episodios. La temporada se dividirá en dos partes, la primera de las cuales se lanzó el
24 de enero de 2020, con el segundo conjunto de 8 episodios que se lanzarán a fines de 2020. <br>
En julio de 2020, Netflix anunció que la próxima cuarta parte sería la última.
